package condition_iteration;

public class IfElse {

	public static void main(String[] args) {

		int a = 10;

		// if 조건
		if (a == 10) {
			System.out.println("a는 10이다.");
		} else {
			System.out.println("a는 10이 아니다.");
		}

		int b = 21;

		if (b % 2 == 0) {
			System.out.println("b는 짝수");
		} else {
			System.out.println("b는 홀수");
		}

	} // ctrl + shift + F = 자동 정렬

}
